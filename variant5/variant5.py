# Отримуємо число від користувача
n = int(input("Введіть число: "))

# Перевіряємо парність
if n % 2 == 0:
    print("Число парне.")

    # Добуток всіх чисел від 1 до n, кратних 3
    product = 1
    for i in range(1, n + 1):
        if i % 3 == 0:
            product *= i
    print(f"Добуток чисел від 1 до {n}, кратних 3: {product}")

else:
    print("Число непарне.")

    # Сума всіх чисел від 1 до n, кратних 3
    total = 0
    for i in range(1, n + 1):
        if i % 3 == 0:
            total += i
    print(f"Сума чисел від 1 до {n}, кратних 3: {total}")